#include <cstdio>
#define m 10007
int main(){
  long long int i,j,k,sum=0,T[1000][10],N;
  scanf("%d",&N);
  for(i=0;i<10;i++) T[0][i]=1;

  for(i=1;i<N;i++){
    for(j=0;j<10;j++){
      for(k=j;k<10;k++){
        T[i][j]= (T[i][j]+T[i-1][k])%m;
      }
    }
  }
  //for(i=0;i<10;i++) printf("%d\n",T[N-1][i]);
  for(i=0;i<10;i++) sum=(sum+T[N-1][i])%m;
  printf("%d",sum);
}
